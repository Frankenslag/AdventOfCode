        public static void Run()
        {
            Stack<char> stk = new Stack<char>();

            List<long> completedScores = new List<long>();

            int scoreCorrupted = 0;

            foreach (string line in data)
            {
                bool corrupted = false;

                stk.Clear();

                foreach (char c in line)
                {
                    if (c == '[' || c == '(' || c == '{' || c == '<')
                    {
                        stk.Push(c);
                    }
                    else if (c == ']' || c == ')' || c == '}' || c == '>')
                    {
                        char cPop = stk.Pop();

                        corrupted = (c == ']' && cPop != '[') || (c == ')' && cPop != '(') || (c == '}' && cPop != '{') || (c == '>' && cPop != '<');

                        if (corrupted)
                        {
                            scoreCorrupted += c switch { ']' => 57, ')' => 3, '}' => 1197, '>' => 25137, _ => 0 };
                            break;
                        }
                    }

                }

                if (!corrupted)
                {
                    long scoreCompleted = 0;

                    while (stk.Count > 0)
                    {
                        scoreCompleted *= 5;
                        scoreCompleted += stk.Pop() switch {'(' => 1, '[' => 2, '{' => 3, '<' => 4, _ => 0 };
                    }

                    completedScores.Add(scoreCompleted);
                }
            }


            Console.WriteLine($"Day 10 Part 1 Answer is Corrupted Score {scoreCorrupted}.");
            Console.WriteLine($"Day 10 Part 2 Answer is Completion Score {completedScores.OrderBy(s => s).ToArray()[completedScores.Count / 2]}.");
        }
