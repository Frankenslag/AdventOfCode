        private static void Execute(string[] inputdata)
        {
            HashSet<(int pinA, int pinB, int pinC, int pinD, int pinE)> locks = [];
            HashSet<(int pinA, int pinB, int pinC, int pinD, int pinE)> keys = [];

            foreach (List<(int key, int maxRow, int minRow)> schematic in inputdata.Select((line, idx) => (line, idx)).Where(v => !string.IsNullOrWhiteSpace(v.line)).GroupBy(v => v.idx / 8)
                         .Select(g => g.Select((s, rowIdx) => (s.line, rowIdx)).SelectMany(s => s.line.Select((c, colIdx) => (c, colIdx)).Where(c => c.c == '#')
                             .Select(c => (c.colIdx, s.rowIdx))).GroupBy(p => p.colIdx).Select(g2 => (g2.Key, g2.Max(x => x.rowIdx), g2.Min(x => x.rowIdx))).OrderBy(p => p.Key).ToList()))
            {
                if (schematic.Count(s => s.minRow == 0) == 5)
                {
                    locks.Add((schematic[0].maxRow, schematic[1].maxRow, schematic[2].maxRow, schematic[3].maxRow, schematic[4].maxRow));
                }
                else if (schematic.Count(s => s.maxRow == 6) == 5)
                {
                    keys.Add((6 - schematic[0].minRow, 6 - schematic[1].minRow, 6 - schematic[2].minRow, 6 - schematic[3].minRow, 6 - schematic[4].minRow));
                }
            }

            Console.WriteLine($"Day 25 Part 1 Answer is output {(from key in keys from lck in locks where key.pinA + lck.pinA < 6 && key.pinB + lck.pinB < 6 && key.pinC + lck.pinC < 6 && key.pinD + lck.pinD < 6 && key.pinE + lck.pinE < 6 select key).Count()} combinations.");
        }

        public static void Run() => Execute(Data);
